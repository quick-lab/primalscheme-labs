				<li><strong>What does scheme status mean?</strong></li>
			</nav>
		</header>

		<nav>
			<ul>
				<li><span class="pill withdrawn"><b>withdrawn</b></span></li>
				<li>This scheme that has been withdrawn, due to any reason. ie poor performance</li>
			</ul>
		</nav>

		<nav>
			<ul>
				<li><span class="pill deprecated"><b>deprecated</b></span></li>
				<li>This scheme is no longer maintained. Often surpassed by a newer version</li>
			</ul>
		</nav>

		<nav>
			<ul>
				<li><span class="pill autogenerated"><b>autogenerated</b></span></li>
				<li>
					This scheme is generated from data sources public databases using pathogen agnostic
					pipelines
				</li>
			</ul>
		</nav>

		<nav>
			<ul>
				<li><span class="pill draft"><b>draft</b></span></li>
				<li>
					This is a curated scheme, however has not undergone any testing. We hope schemes will
					either progress to tested/valdated or deprecated/withdrawn
				</li>
			</ul>
		</nav>

		<nav>
			<ul>
				<li><span class="pill tested"><b>tested</b></span></li>
				<li>
					This scheme has undergone small scale testing, ie shown to work against small number of
					samples
				</li>
			</ul>
		</nav>

		<nav>
			<ul>
				<li><span class="pill validated"><b>validated</b></span></li>
				<li>
					The highest level of validation. The scheme should have undergone extensive use and
					validation. Often used in public health, or has citations
				</li>
			</ul>
		</nav>
